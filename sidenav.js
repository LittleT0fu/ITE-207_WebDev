 /* Open the sidenav */
 function openNavAbout() {
    document.getElementById("about").style.width = "100%";
}
function openNavSkill() {
    document.getElementById("skill").style.width = "100%";
}
function openNavContact() {
    document.getElementById("contact").style.width = "100%";
}


/* Close/hide the sidenav */
function closeNavAbout() {
    document.getElementById("about").style.width = "0";
}
function closeNavSkill() {
    document.getElementById("skill").style.width = "0";
}
function closeNavContact() {
    document.getElementById("contact").style.width = "0";
}